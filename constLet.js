var name = "Javed Ahmed";
console.log(name.length);

// change the variable value
var name = "javed";
if (name.length > 4) {
    name = "sumi";
}
console.log(name);

// if you don't never change the variable value then we use const not var
const country = "Bangladesh";
console.log(country);

// change the variable value we use let
let city = "Dhaka";
city = "Rajshahi";
console.log(city);